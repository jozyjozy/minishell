#include "libft.h"

long long	ft_atoll(const char *nptr)
{
	long long	i;
	long long	number;
	int			neg;

	i = 0;
	neg = 1;
	number = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || (nptr[i] == 32))
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			neg = -neg;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		number = number * 10 + (nptr[i] -48);
		i++;
	}
	return (number * neg);
}
